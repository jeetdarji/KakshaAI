/**
 * reportGenerator.js
 * 
 * Generates a professional, multi-page PDF analytics report using jsPDF.
 * Charts are captured as images via html2canvas.
 * 
 * Main entry point: generateAnalyticsReport(analyticsData, userData, onProgress)
 */

import jsPDF from 'jspdf';
import html2canvas from 'html2canvas';
import {
  formatPercentage,
  formatScore,
  formatTime,
  formatDate,
  formatDateTime,
  getPerformanceBadge,
  generateInsights,
  createRecommendations,
} from './reportFormatters';

// ─── CONSTANTS ──────────────────────────────────────────────

const PAGE_WIDTH = 210; // A4 mm
const PAGE_HEIGHT = 297;
const MARGIN = 20;
const CONTENT_WIDTH = PAGE_WIDTH - MARGIN * 2;
const FOOTER_Y = PAGE_HEIGHT - 12;

const COLORS = {
  primary: [20, 184, 166],      // teal-500
  primaryDark: [13, 148, 136],  // teal-600
  accent: [45, 212, 191],       // teal-300
  bg: [15, 15, 15],             // near-black
  bgCard: [30, 30, 30],         // card bg
  bgCardLight: [40, 40, 40],
  text: [255, 255, 255],
  textSecondary: [161, 161, 170],
  success: [16, 185, 129],
  warning: [245, 158, 11],
  danger: [239, 68, 68],
  physics: [59, 130, 246],
  chemistry: [16, 185, 129],
  maths: [249, 115, 22],
};

// ─── HELPERS ────────────────────────────────────────────────

/** Set fill + text color from an RGB array. */
const setColor = (doc, rgb) => {
  doc.setTextColor(rgb[0], rgb[1], rgb[2]);
};

const setFill = (doc, rgb) => {
  doc.setFillColor(rgb[0], rgb[1], rgb[2]);
};

const setDraw = (doc, rgb) => {
  doc.setDrawColor(rgb[0], rgb[1], rgb[2]);
};

/** Draw a rounded rectangle. */
const roundedRect = (doc, x, y, w, h, r, style = 'F') => {
  doc.roundedRect(x, y, w, h, r, r, style);
};

/** Draw page background. */
const drawPageBg = (doc) => {
  setFill(doc, COLORS.bg);
  doc.rect(0, 0, PAGE_WIDTH, PAGE_HEIGHT, 'F');
};

/** Add header bar (used on all pages except cover). */
const addHeader = (doc, title) => {
  // Header bar
  setFill(doc, COLORS.primaryDark);
  doc.rect(0, 0, PAGE_WIDTH, 18, 'F');

  doc.setFont('helvetica', 'bold');
  doc.setFontSize(11);
  setColor(doc, COLORS.text);
  doc.text('KakshaAI', MARGIN, 12);

  doc.setFont('helvetica', 'normal');
  doc.setFontSize(10);
  doc.text(title, PAGE_WIDTH - MARGIN, 12, { align: 'right' });
};

/** Add footer with page number. */
const addFooter = (doc, pageNum, totalPages) => {
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(8);
  setColor(doc, COLORS.textSecondary);
  doc.text(
    `Page ${pageNum} of ${totalPages}`,
    PAGE_WIDTH / 2,
    FOOTER_Y,
    { align: 'center' }
  );
  doc.text('Generated by KakshaAI', MARGIN, FOOTER_Y);
  doc.text(formatDateTime(new Date()), PAGE_WIDTH - MARGIN, FOOTER_Y, { align: 'right' });
};

/** Start a new page with background, header, and return starting Y. */
const newPage = (doc, title) => {
  doc.addPage();
  drawPageBg(doc);
  addHeader(doc, title);
  return 26; // Y position after header
};

/** Draw a section title and return Y after it. */
const sectionTitle = (doc, title, y) => {
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(16);
  setColor(doc, COLORS.primary);
  doc.text(title, MARGIN, y);

  // Underline
  setDraw(doc, COLORS.primary);
  doc.setLineWidth(0.5);
  doc.line(MARGIN, y + 2, MARGIN + doc.getTextWidth(title), y + 2);

  return y + 10;
};

/** Draw a KPI card. */
const drawKPICard = (doc, x, y, w, h, label, value, sublabel) => {
  setFill(doc, COLORS.bgCard);
  roundedRect(doc, x, y, w, h, 3);

  // Value
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(20);
  setColor(doc, COLORS.accent);
  doc.text(String(value), x + w / 2, y + h / 2 - 2, { align: 'center' });

  // Label
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(9);
  setColor(doc, COLORS.textSecondary);
  doc.text(label, x + w / 2, y + h / 2 + 8, { align: 'center' });

  // Sublabel
  if (sublabel) {
    doc.setFontSize(7);
    doc.text(sublabel, x + w / 2, y + h / 2 + 14, { align: 'center' });
  }
};

/** Draw a horizontal bar chart row. */
const drawBar = (doc, x, y, maxW, pct, color, label, valueText) => {
  const barH = 8;

  // Label
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(10);
  setColor(doc, COLORS.text);
  doc.text(label, x, y + barH / 2 + 1);

  const barX = x + 45;
  const barW = maxW - 65;

  // Track
  setFill(doc, COLORS.bgCardLight);
  roundedRect(doc, barX, y, barW, barH, 2);

  // Fill
  const fillW = Math.max(2, (pct / 100) * barW);
  setFill(doc, color);
  roundedRect(doc, barX, y, fillW, barH, 2);

  // Value
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(10);
  setColor(doc, color);
  doc.text(valueText, barX + barW + 5, y + barH / 2 + 1);
};

/** Draw a simple table. */
const drawTable = (doc, x, y, headers, rows, colWidths) => {
  const rowH = 8;

  // Header
  setFill(doc, COLORS.bgCard);
  doc.rect(x, y, CONTENT_WIDTH, rowH, 'F');
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(9);
  setColor(doc, COLORS.accent);

  let cx = x + 3;
  headers.forEach((h, i) => {
    doc.text(h, cx, y + rowH / 2 + 1);
    cx += colWidths[i];
  });

  y += rowH;

  // Rows
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(9);
  rows.forEach((row, ri) => {
    if (ri % 2 === 0) {
      setFill(doc, COLORS.bgCardLight);
      doc.rect(x, y, CONTENT_WIDTH, rowH, 'F');
    }

    setColor(doc, COLORS.text);
    cx = x + 3;
    row.forEach((cell, ci) => {
      doc.text(String(cell), cx, y + rowH / 2 + 1);
      cx += colWidths[ci];
    });

    y += rowH;
  });

  return y + 4;
};

// ─── CHART CAPTURE ──────────────────────────────────────────

/**
 * Capture a DOM element as a JPEG data URL using html2canvas.
 * @param {string} elementId - The id of the element to capture.
 * @returns {Promise<string|null>} JPEG data URL or null on failure.
 */
export async function captureChartAsImage(elementId) {
  try {
    const el = document.getElementById(elementId);
    if (!el) {
      console.warn(`Chart element #${elementId} not found, skipping.`);
      return null;
    }

    const canvas = await html2canvas(el, {
      backgroundColor: '#0f0f0f',
      scale: 1.5,       // balance quality vs. size
      useCORS: true,
      logging: false,
    });

    return canvas.toDataURL('image/jpeg', 0.82);
  } catch (err) {
    console.error(`Failed to capture chart #${elementId}:`, err);
    return null;
  }
}

