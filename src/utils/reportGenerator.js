// ─── HEX TO RGB HELPER ─────────────────────────────────────

function hexToRgb(hex) {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result
    ? [parseInt(result[1], 16), parseInt(result[2], 16), parseInt(result[3], 16)]
    : COLORS.text;
}

// ─── MAIN ENTRY POINT ──────────────────────────────────────

/**
 * Generate a comprehensive PDF analytics report.
 *
 * @param {object} analyticsData - The full analytics data object from Analytics.jsx state.
 * @param {object} userData      - Current user object (email, name / user_metadata).
 * @param {function} onProgress  - Optional callback (progressPercent: number) => void.
 * @returns {Promise<void>} Triggers browser download of the PDF.
 */
export async function generateAnalyticsReport(analyticsData, userData, onProgress) {
  const progress = (pct) => {
    if (typeof onProgress === 'function') onProgress(pct);
  };

  if (!analyticsData) {
    throw new Error('No analytics data provided.');
  }

  // ── Create document ───────────────────────────────────────
  const doc = new jsPDF({
    orientation: 'portrait',
    unit: 'mm',
    format: 'a4',
    compress: true,
  });

  progress(5);

  // ── Page 1: Cover ─────────────────────────────────────────
  addCoverPage(doc, userData);
  progress(10);

  // ── Page 2: Summary ───────────────────────────────────────
  addSummaryPage(doc, analyticsData.kpi, analyticsData.subjectPerformance);
  progress(18);

  // ── Capture charts ────────────────────────────────────────
  progress(20);

  const chartIds = [
    'report-subject-performance-chart',
    'report-score-trends-chart',
    'report-weekly-activity-chart',
    'report-time-distribution-chart',
  ];

  const chartImages = {};
  for (let i = 0; i < chartIds.length; i++) {
    chartImages[chartIds[i]] = await captureChartAsImage(chartIds[i]);
    progress(20 + Math.round(((i + 1) / chartIds.length) * 25));
  }

  progress(48);

  // ── Page 3: Subject Performance ───────────────────────────
  addSubjectPerformancePage(
    doc,
    analyticsData.subjectPerformance,
    chartImages['report-subject-performance-chart']
  );
  progress(55);

  // ── Page 4: Score Trends ──────────────────────────────────
  addScoreTrendsPage(
    doc,
    analyticsData.scoreTrends,
    chartImages['report-score-trends-chart']
  );
  progress(62);

  // ── Page 5: Weekly Activity ───────────────────────────────
  addWeeklyActivityPage(
    doc,
    analyticsData.weeklyActivity,
    chartImages['report-weekly-activity-chart']
  );
  progress(68);

  // ── Page 6: Topic Analysis ────────────────────────────────
  addTopicAnalysisPage(doc, analyticsData.topicAnalysis);
  progress(74);

  // ── Page 7: MHT-CET Prediction ────────────────────────────
  addPredictionPage(doc, analyticsData.prediction);
  progress(80);

  // ── Page 8: Time Management ───────────────────────────────
  addTimeManagementPage(
    doc,
    analyticsData.timeDistribution,
    analyticsData.timeManagement,
    chartImages['report-time-distribution-chart']
  );
  progress(85);

  // ── Page 9: Error Patterns ────────────────────────────────
  addErrorPatternsPage(doc, analyticsData.errorPatterns);
  progress(90);

  // ── Page 10: Goals ────────────────────────────────────────
  addGoalsPage(doc, analyticsData.goals);
  progress(94);

  // ── Page 11: Final Summary ────────────────────────────────
  addFinalSummaryPage(doc, analyticsData);
  progress(97);

  // ── Add page numbers to all pages (except cover) ─────────
  const totalPages = doc.internal.getNumberOfPages();
  for (let i = 2; i <= totalPages; i++) {
    doc.setPage(i);
    addFooter(doc, i - 1, totalPages - 1);
  }

  progress(99);

  // ── Download ──────────────────────────────────────────────
  const dateStr = new Date().toISOString().slice(0, 10);
  doc.save(`KakshaAI-Analytics-Report-${dateStr}.pdf`);

  progress(100);
}

export default generateAnalyticsReport;
