/** Page 2: Summary Dashboard */
function addSummaryPage(doc, kpi, subjectPerformance) {
  let y = newPage(doc, 'Summary Dashboard');

  y = sectionTitle(doc, 'Key Performance Indicators', y);

  const cardW = (CONTENT_WIDTH - 9) / 4;
  const cardH = 34;

  drawKPICard(doc, MARGIN, y, cardW, cardH,
    'Tests Completed', kpi?.totalTests ?? 0, 'Total mock tests');

  drawKPICard(doc, MARGIN + cardW + 3, y, cardW, cardH,
    'Percentile Rank', formatPercentage(kpi?.percentileRank), 'Among all users');

  const impSign = (kpi?.overallImprovement ?? 0) >= 0 ? '+' : '';
  drawKPICard(doc, MARGIN + (cardW + 3) * 2, y, cardW, cardH,
    'Improvement', `${impSign}${formatPercentage(kpi?.overallImprovement)}`, 'Score trend');

  const bestTopic = kpi?.bestScoringTopic;
  drawKPICard(doc, MARGIN + (cardW + 3) * 3, y, cardW, cardH,
    'Best Topic', bestTopic?.name ?? 'N/A',
    bestTopic ? `${formatPercentage(bestTopic.accuracy)} accuracy` : '');

  y += cardH + 12;

  // Overall badge
  const avgPct = subjectPerformance?.length
    ? subjectPerformance.reduce((s, sp) => s + (sp.percentage || 0), 0) / subjectPerformance.length
    : 0;
  const badge = getPerformanceBadge(avgPct);

  y = sectionTitle(doc, 'Overall Assessment', y);
  setFill(doc, COLORS.bgCard);
  roundedRect(doc, MARGIN, y, CONTENT_WIDTH, 24, 3);

  doc.setFont('helvetica', 'bold');
  doc.setFontSize(14);
  setColor(doc, avgPct >= 70 ? COLORS.success : avgPct >= 50 ? COLORS.warning : COLORS.danger);
  doc.text(badge, MARGIN + 10, y + 10);

  doc.setFont('helvetica', 'normal');
  doc.setFontSize(10);
  setColor(doc, COLORS.textSecondary);
  doc.text(
    `Overall average: ${formatPercentage(avgPct)} across ${subjectPerformance?.length ?? 0} subjects.`,
    MARGIN + 10, y + 18
  );

  y += 32;

  // Quick insights
  y = sectionTitle(doc, 'Quick Insights', y);
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(10);
  setColor(doc, COLORS.text);

  const insights = generateInsights({ kpi, subjectPerformance });
  insights.forEach((ins) => {
    if (y > PAGE_HEIGHT - 30) return;
    doc.text(`•  ${ins}`, MARGIN + 4, y);
    y += 7;
  });

  return y;
}

/** Page 3: Subject Performance */
function addSubjectPerformancePage(doc, subjectPerformance, chartImage) {
  let y = newPage(doc, 'Subject Performance');
  y = sectionTitle(doc, 'Subject-wise Breakdown', y);

  // Chart image
  if (chartImage) {
    try {
      doc.addImage(chartImage, 'JPEG', MARGIN, y, CONTENT_WIDTH, 80);
      y += 85;
    } catch (e) {
      console.error('Failed to add subject chart image:', e);
      doc.setFont('helvetica', 'italic');
      doc.setFontSize(10);
      setColor(doc, COLORS.textSecondary);
      doc.text('Chart could not be rendered.', MARGIN, y + 10);
      y += 20;
    }
  }

  // Bar chart fallback / detail
  if (subjectPerformance?.length) {
    y = sectionTitle(doc, 'Detailed Statistics', y);

    subjectPerformance.forEach((sp) => {
      if (y > PAGE_HEIGHT - 40) return;
      const color = sp.color
        ? sp.color.startsWith('#')
          ? hexToRgb(sp.color)
          : COLORS.text
        : COLORS.primary;

      drawBar(doc, MARGIN, y, CONTENT_WIDTH, sp.percentage ?? 0, color,
        sp.subject ?? 'Unknown',
        `${formatPercentage(sp.percentage)}  (${formatScore(sp.correctAnswers, sp.totalQuestions)})`
      );
      y += 14;
    });

    // Table
    y += 4;
    const headers = ['Subject', 'Accuracy', 'Correct', 'Total', 'Badge'];
    const colWidths = [40, 30, 30, 30, 40];
    const rows = subjectPerformance.map((sp) => [
      sp.subject ?? '',
      formatPercentage(sp.percentage),
      String(sp.correctAnswers ?? 0),
      String(sp.totalQuestions ?? 0),
      getPerformanceBadge(sp.percentage),
    ]);
    y = drawTable(doc, MARGIN, y, headers, rows, colWidths);
  }

  return y;
}

/** Page 4: Score Trends */
function addScoreTrendsPage(doc, scoreTrends, chartImage) {
  let y = newPage(doc, 'Score Trends');
  y = sectionTitle(doc, 'Score Progression Over Time', y);

  if (chartImage) {
    try {
      doc.addImage(chartImage, 'JPEG', MARGIN, y, CONTENT_WIDTH, 90);
      y += 95;
    } catch (e) {
      console.error('Failed to add score trends chart:', e);
      y += 10;
    }
  }

  // Highlights
  if (scoreTrends?.length >= 2) {
    y = sectionTitle(doc, 'Trend Highlights', y);

    const subjects = ['physics', 'chemistry', 'mathematics'];
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(10);

    subjects.forEach((subj) => {
      if (y > PAGE_HEIGHT - 25) return;
      const vals = scoreTrends
        .map((s) => s[subj])
        .filter((v) => v != null);
      if (vals.length < 2) return;

      const first = vals[0];
      const last = vals[vals.length - 1];
      const diff = last - first;
      const arrow = diff >= 0 ? '↑' : '↓';
      const clr = diff >= 0 ? COLORS.success : COLORS.danger;

      setColor(doc, clr);
      const name = subj.charAt(0).toUpperCase() + subj.slice(1);
      doc.text(
        `${arrow} ${name}: ${first}% → ${last}% (${diff >= 0 ? '+' : ''}${diff.toFixed(1)}%)`,
        MARGIN + 4, y
      );
      y += 7;
    });
  } else {
    doc.setFont('helvetica', 'italic');
    doc.setFontSize(10);
    setColor(doc, COLORS.textSecondary);
    doc.text('Not enough data to show trends. Take more tests!', MARGIN + 4, y);
    y += 10;
  }

  return y;
}

